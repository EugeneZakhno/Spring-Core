package io.github.eugenezakhno;

public class SimpleBean {

    private int id;
    private String name;

    public SimpleBean(String name) {
        this.name = name;
    }

    public SimpleBean() {
    }

    public SimpleBean(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "SimpleBean{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
